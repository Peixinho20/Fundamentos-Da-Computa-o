'''
Desenvolva um algoritmo que receba quatro números inteiros, 
calcule e mostre a soma desses números.
'''
n1 = input("Digite o primeiro inteiro: ")
n2 = input("Digite o segundo inteiro: ")
n3 = input("Digite o terceiro inteiro: ")
